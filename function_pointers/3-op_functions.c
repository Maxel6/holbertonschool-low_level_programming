#include "3-calc.h"
/**
 * op_add - add a and b
 * @a: is an integer
 * @b: is an integer
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub a and b
 * @a: is an integer
 * @b: is an integer
 * Return: the difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplie a and b
 * @a: is an integer
 * @b: is an integer
 * Return: the product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div a and b
 * @a: is an integer
 * @b: is an integer
 * Return: the result of the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod a and b
 * @a: is an integer
 * @b: is an integer
 * Return: the remainder of the divisionof a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
